def dfs(x, y):
    visit.add((x, y))
    if x == row - 1:
        return True
    if x - 1 >=0 and (x - 1, y) not in visit and grid[x - 1][y] == 0:
        if dfs(x - 1, y):
            return True
    if x + 1 < row and (x + 1, y) not in visit and grid[x + 1][y] == 0:
        if dfs(x + 1, y):
            return True
    if y + 1 < col and (x, y + 1) not in visit and grid[x][y + 1] == 0:
        if dfs(x, y + 1):
            return True
    if y - 1 >= 0 and (x, y - 1) not in visit and grid[x][y - 1] == 0:
        if dfs(x, y - 1):
            return True
    return False


